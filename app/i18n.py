def translate_text(text, language="en"):
    if text is None:
        return ""
    text = str(text)
    if language == "ml":
        return MALAYALAM_TRANSLATIONS.get(text, text)
    return text


def format_date_text(value, language="en"):
    if not value:
        return ""
    if isinstance(value, datetime):
        value = value.date()
    if language == "ml":
        return f"{value.day:02d} {MALAYALAM_MONTHS[value.month - 1]} {value.year}"
    return value.strftime("%d %b %Y")


def format_time_text(value, language="en"):
    if not value:
        return ""
    if language != "ml":
        return value.strftime("%I:%M %p")

    hour = value.hour
    if hour < 5:
        period = "പുലർച്ചെ"
    elif hour < 12:
        period = "രാവിലെ"
    elif hour < 16:
        period = "ഉച്ചയ്ക്ക്"
    elif hour < 19:
        period = "വൈകിട്ട്"
    else:
        period = "രാത്രി"
    return f"{value.strftime('%I:%M')} {period}"
